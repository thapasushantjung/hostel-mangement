    public function __construct(int $year, int $month, int $day)
    {
        $this->year = $year;
        $this->month = $month;
        $this->day = $day;
    }

    public static function now(): self
    {
        return self::fromAD(now());
    }

    public static function fromAD(Carbon $date): self
    {
        $refDate = Carbon::create(self::REF_AD_YEAR, self::REF_AD_MONTH, self::REF_AD_DAY);
        $diffDays = $refDate->diffInDays($date, false);

        $year = 2000;
        $month = 1;
        $day = 1;

        if ($diffDays >= 0) {
            // Forward from 2000
            while ($diffDays > 0) {
                $daysInYear = array_sum(self::$calendarData[$year]);
                if ($diffDays >= $daysInYear) {
                    $diffDays -= $daysInYear;
                    $year++;

                    continue;
                }

                $daysInCurrentMonth = self::$calendarData[$year][$month - 1];
                if ($diffDays >= $daysInCurrentMonth) {
                    $diffDays -= $daysInCurrentMonth;
                    $month++;

                    continue;
                }

                $day += $diffDays;
                $diffDays = 0;
            }
        } else {
            // Backward from 2000 (Not supported yet for simplicity, staying > 2000)
            // Handle if date < 1943-04-14 if needed, but app likely doesn't need historical data > 80 years
            // Reset to 2000-01-01
        }

        return new self($year, $month, $day);
    }

    public function toAD(): Carbon
    {
        $refDate = Carbon::create(self::REF_AD_YEAR, self::REF_AD_MONTH, self::REF_AD_DAY);
        $totalDays = 0;

        // Add days for full years
        for ($y = 2000; $y < $this->year; $y++) {
            $totalDays += array_sum(self::$calendarData[$y] ?? self::$calendarData[2000]); // Fallback if out of range
        }

        // Add days for full months in current year
        for ($m = 1; $m < $this->month; $m++) {
            $totalDays += self::$calendarData[$this->year][$m - 1];
        }

        // Add days
        $totalDays += ($this->day - 1);

        return $refDate->addDays($totalDays);
    }

    public function format(string $format = 'Y-m-d'): string
    {
        $formatted = $format;
        $formatted = str_replace('Y', (string) $this->year, $formatted);
        $formatted = str_replace('m', str_pad($this->month, 2, '0', STR_PAD_LEFT), $formatted);
        $formatted = str_replace('d', str_pad($this->day, 2, '0', STR_PAD_LEFT), $formatted);

        // Add Nepali month names if needed
        $monthNames = ['Baisakh', 'Jestha', 'Asar', 'Shrawan', 'Bhadra', 'Aswin', 'Kartik', 'Mangsir', 'Poush', 'Magh', 'Falgun', 'Chaitra'];
        $formatted = str_replace('F', $monthNames[$this->month - 1], $formatted);

        return $formatted;
    }

    public function isFirstDayOfMonth(): bool
    {
        return $this->day === 1;
    }

    public function daysInMonth(): int
    {
        return self::$calendarData[$this->year][$this->month - 1] ?? 30;
    }

    public function startOfMonthAsAD(): Carbon
    {
        $start = new self($this->year, $this->month, 1);

        return $start->toAD();
    }

    public function endOfMonthAsAD(): Carbon
    {
        $end = new self($this->year, $this->month, $this->daysInMonth());

        return $end->toAD();
    }
